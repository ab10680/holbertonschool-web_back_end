#!/usr/bin/env python3
"""
This module contains a function that adds two float numbers.
"""


def add(a: float, b: float) -> float:
    """
    Returns the sum of two float numbers.

    Args:
        a (float): First number.
        b (float): Second number.

    Returns:
        float: The sum of a and b.
    """
    return a + b
